#include <stdio.h>
#include <stdint.h>
#include <string.h>

/* Minimal aarch64 ELF Hello World */
int main() {
    FILE *f = fopen("hello_world", "wb");
    if (!f) return 1;

    /* ELF Header (64 bytes) */
    uint8_t elf_header[] = {
        0x7f, 0x45, 0x4c, 0x46,     /* Magic */
        0x02,                         /* 64-bit */
        0x01,                         /* little-endian */
        0x01,                         /* version */
        0x00,                         /* ABI */
        0x00, 0x00, 0x00, 0x00,     /* padding */
        0x00, 0x00, 0x00, 0x00,
        0x02, 0x00,                 /* e_type: executable */
        0xb7, 0x00,                 /* e_machine: aarch64 */
        0x01, 0x00, 0x00, 0x00,     /* e_version */
        0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,  /* e_entry: 0x400000 */
        0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  /* e_phoff: 0x40 */
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  /* e_shoff: 0 */
        0x00, 0x00, 0x00, 0x00,     /* e_flags */
        0x40, 0x00,                 /* e_ehsize: 64 */
        0x38, 0x00,                 /* e_phentsize: 56 */
        0x01, 0x00,                 /* e_phnum: 1 */
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00
    };

    /* Program Header (56 bytes) */
    uint8_t prog_header[] = {
        0x01, 0x00, 0x00, 0x00,     /* p_type: PT_LOAD */
        0x05, 0x00, 0x00, 0x00,     /* p_flags: PF_X|PF_R */
        0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  /* p_offset: 0x100 */
        0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,  /* p_vaddr: 0x400000 */
        0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,  /* p_paddr: 0x400000 */
        0x2d, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  /* p_filesz: 45 */
        0x2d, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  /* p_memsz: 45 */
        0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00   /* p_align: 0x1000 */
    };

    /* Padding to 256 bytes */
    uint8_t padding[256 - sizeof(elf_header) - sizeof(prog_header)];
    memset(padding, 0, sizeof(padding));

    /* aarch64 code: Hello World (32 bytes) */
    uint8_t code[] = {
        0xa0, 0x00, 0x80, 0xd2,     /* mov x0, #1 */
        0x01, 0x00, 0x00, 0x10,     /* adr x1, msg */
        0xa2, 0x01, 0x80, 0xd2,     /* mov x2, #13 */
        0x08, 0x08, 0x80, 0xd2,     /* mov x8, #64 */
        0x01, 0x00, 0x00, 0xd4,     /* svc #0 */
        0x00, 0x00, 0x80, 0xd2,     /* mov x0, #0 */
        0x08, 0xb9, 0x80, 0xd2,     /* mov x8, #93 */
        0x01, 0x00, 0x00, 0xd4      /* svc #0 */
    };

    /* Data: "Hello, World!\n" (14 bytes) */
    uint8_t data[] = {
        0x48, 0x65, 0x6c, 0x6c, 0x6f, 0x2c, 0x20, 0x57, 0x6f, 0x72, 0x6c, 0x64, 0x21, 0x0a
    };

    /* Write all sections */
    fwrite(elf_header, 1, sizeof(elf_header), f);
    fwrite(prog_header, 1, sizeof(prog_header), f);
    fwrite(padding, 1, sizeof(padding), f);
    fwrite(code, 1, sizeof(code), f);
    fwrite(data, 1, sizeof(data), f);

    fclose(f);
    printf("✓ ELF binary created: hello_world (%lu bytes)\n",
           sizeof(elf_header) + sizeof(prog_header) + sizeof(padding) + sizeof(code) + sizeof(data));
    return 0;
}
